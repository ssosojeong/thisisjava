package sec4;

public class BitLogicExample {

	public static void main(String[] args) {
		System.out.println("45 & 25 = " + (45 & 25)); //and
		System.out.println("45 | 25 = " + (45 | 25)); //or
		System.out.println("45 ^ 25 = " + (45 ^ 25)); //xor 
		System.out.println("~45 = " + (~45)); //not
	}

}

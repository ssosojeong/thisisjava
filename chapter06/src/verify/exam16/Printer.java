package verify.exam16;

public class Printer {
	
	//타입 int
	public void println(int x) {
		System.out.println(x);
	}
	
	//타입 boolean
	public void println(boolean x) {
		System.out.println(x);
	}
	
	//타입 double
	public void println(double x) {
		System.out.println(x);
	}
		
	//타입 String
	public void println(String x) {
		System.out.println(x);
	}	

}
